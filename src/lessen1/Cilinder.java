public class Cilinder {
    public static void main(String[] args) {
        double d = 4.88;
        double h = 1.22;
        double p = 3.14;
        double r = d / 2;
        double сylinderPerimeter = (2 * d) + (2 * h);
        System.out.println(сylinderPerimeter);
        double areaCilinder = 2 * p * r * h;
        System.out.println(areaCilinder);


    }
}
